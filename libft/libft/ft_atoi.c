#include "libft.h"

long	ft_atoi(const char *str)
{
	long	r;
	long	sign;

	r = 0;
	sign = 1;
	while (*str == '\t' || *str == '\n' || *str == '\v' || *str == '\f'
		|| *str == '\r' || *str == ' ')
		++str;
	if (*str == '-')
		sign = -1;
	if (*str == '-' || *str == '+')
		++str;
	while (*str && *str >= '0' && *str <= '9')
	{
		r = r * 10 + (*str - '0');
		++str;
	}
	return (r * sign);
}
